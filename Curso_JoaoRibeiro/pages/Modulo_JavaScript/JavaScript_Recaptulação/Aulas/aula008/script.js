/**
 * Aqui está uma lista dos operadores em JavaScript e seus respectivos usos:

1. Operadores Aritméticos:
   - `+` (adição): Soma dois valores ou concatena strings.
   - `-` (subtração): Subtrai um valor de outro.
   - `*` (multiplicação): Multiplica dois valores.
   - `/` (divisão): Divide um valor pelo outro.
   - `%` (módulo): Retorna o resto da divisão entre dois valores.
   - `++` (incremento): Incrementa o valor de uma variável em 1.
   - `--` (decremento): Decrementa o valor de uma variável em 1.

2. Operadores de Atribuição:
   - `=` (atribuição): Atribui um valor a uma variável.
   - `+=` (atribuição com adição): Atribui o resultado da adição à variável.
   - `-=` (atribuição com subtração): Atribui o resultado da subtração à variável.
   - `*=` (atribuição com multiplicação): Atribui o resultado da multiplicação à variável.
   - `/=` (atribuição com divisão): Atribui o resultado da divisão à variável.
   - `%=` (atribuição com módulo): Atribui o resultado do módulo à variável.

3. Operadores de Comparação:
   - `==` (igual): Verifica se dois valores são iguais (realizando conversão de tipos se necessário).
   - `===` (estritamente igual): Verifica se dois valores são iguais (sem conversão de tipos).
   - `!=` (diferente): Verifica se dois valores são diferentes (realizando conversão de tipos se necessário).
   - `!==` (estritamente diferente): Verifica se dois valores são diferentes (sem conversão de tipos).
   - `>` (maior que): Verifica se um valor é maior que outro.
   - `<` (menor que): Verifica se um valor é menor que outro.
   - `>=` (maior ou igual a): Verifica se um valor é maior ou igual a outro.
   - `<=` (menor ou igual a): Verifica se um valor é menor ou igual a outro.

4. Operadores Lógicos:
   - `&&` (e lógico): Retorna verdadeiro se as duas condições são verdadeiras.
   - `||` (ou lógico): Retorna verdadeiro se pelo menos uma das condições é verdadeira.
   - `!` (negação lógica): Inverte o valor de uma expressão lógica.

5. Operadores de Concatenação:
   - `+` (concatenação): Concatena duas strings.

6. Operadores Ternários:
   - `? :` (operador ternário): Avalia uma condição e retorna um valor com base nessa condição.

7. Outros Operadores:
   - `.` (operador de ponto): Acessa propriedades de objetos.
   - `[]` (operador de colchetes): Acessa propriedades de objetos usando uma string ou valor numérico como chave.

Esses são apenas alguns dos operadores disponíveis em JavaScript. Cada operador possui uma função específica e pode ser usado de diferentes maneiras, dependendo do contexto.
 */