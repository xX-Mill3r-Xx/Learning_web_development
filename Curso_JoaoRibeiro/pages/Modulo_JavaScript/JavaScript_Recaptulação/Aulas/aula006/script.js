//Tipos de dados em js

// Aqui estão alguns exemplos de variáveis em JavaScript com seus respectivos tipos de dados:

//1. Variáveis numéricas:
   var idade = 25; // Número inteiro
   var altura = 1.75; // Número de ponto flutuante

//2. Variáveis de texto (strings):
   var nome = "João"; // Texto
   var mensagem = 'Olá, mundo!'; // Texto entre aspas simples

//3. Variáveis booleanas:
   var estaAtivo = true; // Valor booleano verdadeiro
   var possuiConta = false; // Valor booleano falso

//4. Variáveis de array:
   var numeros = [1, 2, 3, 4, 5]; // Array de números
   var frutas = ["maçã", "banana", "laranja"]; // Array de strings

//5. Variáveis de objeto:
   var pessoa = {
     nome: "Maria",
     idade: 30,
     cidade: "São Paulo"
   }; // Objeto com propriedades

   var carro = {
     marca: "Toyota",
     modelo: "Corolla",
     ano: 2021,
     cor: "preto"
   }; // Outro exemplo de objeto

//6. Variáveis nulas e indefinidas:
   var valorNulo = null; // Valor nulo intencionalmente
   var valorIndefinido; // Valor indefinido (não atribuído)

/*Esses são apenas alguns exemplos de variáveis em JavaScript e seus tipos de dados. O JavaScript é uma linguagem dinamicamente tipada, o que significa que as variáveis não têm um tipo fixo e podem mudar durante a execução do programa. É importante ter em mente que esses exemplos são apenas uma introdução e existem muitos outros tipos e recursos em JavaScript para explorar.*/